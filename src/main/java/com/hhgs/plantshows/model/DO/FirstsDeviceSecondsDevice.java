package com.hhgs.plantshows.model.DO;

public class FirstsDeviceSecondsDevice {

    private int firstsDeviceId;

    private int secondsDeviceId;

    private String secondsDeviceName;

    private String plantName;

    public int getFirstsDeviceId() {
        return firstsDeviceId;
    }

    public void setFirstsDeviceId(int firstsDeviceId) {
        this.firstsDeviceId = firstsDeviceId;
    }

    public int getSecondsDeviceId() {
        return secondsDeviceId;
    }

    public void setSecondsDeviceId(int secondsDeviceId) {
        this.secondsDeviceId = secondsDeviceId;
    }

    public String getSecondsDeviceName() {
        return secondsDeviceName;
    }

    public void setSecondsDeviceName(String secondsDeviceName) {
        this.secondsDeviceName = secondsDeviceName;
    }

    public String getPlantName() {
        return plantName;
    }

    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }
}
