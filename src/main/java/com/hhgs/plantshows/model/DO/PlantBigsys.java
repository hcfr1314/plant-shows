package com.hhgs.plantshows.model.DO;

public class PlantBigsys {

    private Integer bigsysId;

    private String bigsysName;

    private String plantName;

    public Integer getBigsysId() {
        return bigsysId;
    }

    public void setBigsysId(Integer bigsysId) {
        this.bigsysId = bigsysId;
    }

    public String getBigsysName() {
        return bigsysName;
    }

    public void setBigsysName(String bigsysName) {
        this.bigsysName = bigsysName;
    }

    public String getPlantName() {
        return plantName;
    }

    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }
}
