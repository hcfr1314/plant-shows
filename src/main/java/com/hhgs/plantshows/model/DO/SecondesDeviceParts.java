package com.hhgs.plantshows.model.DO;

public class SecondesDeviceParts {

    private int secondsDeviceId;

    private int partsId;

    private String partsName;

    private String plantName;

    public int getSecondsDeviceId() {
        return secondsDeviceId;
    }

    public void setSecondsDeviceId(int secondsDeviceId) {
        this.secondsDeviceId = secondsDeviceId;
    }

    public int getPartsId() {
        return partsId;
    }

    public void setPartsId(int partsId) {
        this.partsId = partsId;
    }

    public String getPartsName() {
        return partsName;
    }

    public void setPartsName(String partsName) {
        this.partsName = partsName;
    }

    public String getPlantName() {
        return plantName;
    }

    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }
}
